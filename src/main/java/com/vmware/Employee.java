package com.vmware;

public class Employee {
	private String name;
	private String ssn;

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setSocialSecurityNumber(String ssn) {
		this.ssn = ssn;
	}

	public String getSocialSecurityNumber() {
		return ssn;
	}
}











